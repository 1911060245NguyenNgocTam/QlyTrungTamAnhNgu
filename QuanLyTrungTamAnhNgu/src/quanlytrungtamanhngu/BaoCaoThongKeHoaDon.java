    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        XSSFWorkbook workbook = new XSSFWorkbook();

        XSSFSheet sheet = workbook.createSheet("Thong ke hoa don");

        XSSFRow row;

        row = null;
        Cell cell = null;
        for (int i = 0; i < tableModel.getRowCount(); i++) {
            row = sheet.createRow(i);
            for (int j = 0; j < tableModel.getColumnCount(); j++) {

                cell = row.createCell(j);
                cell.setCellValue((String) tableModel.getValueAt(i, j));
            }
        }
        //Write the workbook in file system
        FileOutputStream out;
        try {
            out = new FileOutputStream(new File("C:\\Users\\HP\\Desktop\\excelkaka\\thong-ke-hoa-don.xlsx"));
            workbook.write(out);
            out.close();
            System.out.println("Writesheet.xlsx written successfully");
        } catch (FileNotFoundException ex) {
            Logger.getLogger(BaoCaoThongKeHoaDon.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(BaoCaoThongKeHoaDon.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


